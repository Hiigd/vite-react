export const SEISMOGRAM_DATA = Array.from({ length: 100 }, (_, i) => {
  let pWave = 0;
  let sWave = 0;
  let surfaceWave = 0;
  
  // Simulate P-wave arrival at t=20
  if (i >= 20 && i < 35) pWave = (Math.random() - 0.5) * 10;
  
  // Simulate S-wave arrival at t=45
  if (i >= 45 && i < 70) sWave = (Math.random() - 0.5) * 30;

  // Simulate Surface wave at t=75
  if (i >= 75) surfaceWave = (Math.random() - 0.5) * 80;

  return {
    time: i,
    amplitude: pWave + sWave + surfaceWave + (Math.random() - 0.5) * 2 // Background noise
  };
});

